CREATE TABLE logician_code
(
   obs_term    VARCHAR(50) NOT NULL PRIMARY KEY,
   obs_code    VARCHAR(50),
   hd_id       VARCHAR(30),
   data_type   VARCHAR(10),
   cpt_code    VARCHAR(10),
   dose_number VARCHAR(3),
   description VARCHAR(220)
);

CREATE TABLE logician_entity
(
  entity_id      MEDIUMINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  registry_name  VARCHAR(30) NOT NULL, 
  entity_name    VARCHAR(30) NOT NULL
);

CREATE TABLE logician_flowsheet
(
  obs_term       VARCHAR(50) NOT NULL,
  entity_id      MEDIUMINT NOT NULL,
  include_status VARCHAR(1) NOT NULL
);

alter table logician_flowsheet add primary key (obs_term, entity_id);

CREATE TABLE logician_cpt
(
  cpt_code      VARCHAR(10) NOT NULL PRIMARY KEY,
  cpt_label     VARCHAR(50) NOT NULL,
  accept_status VARCHAR(1) NOT NULL
);

